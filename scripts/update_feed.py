import xml.etree.ElementTree as ET
from datetime import datetime
import os
import sys

def update_podcast_feed(title, mp3_filename, description):
    feed_file = 'feed.xml'
    base_url = "https://mommocmoc.github.io/supercow-ai-news-podcast/"
    audio_url = base_url + "audio/" + mp3_filename
    image_url = base_url + "cover.png"
    
    ITUNES_NS = "http://www.itunes.com/dtds/podcast-1.0.dtd"
    ET.register_namespace('itunes', ITUNES_NS)
    
    if not os.path.exists(feed_file):
        rss = ET.Element('rss', version='2.0')
        rss.set('xmlns:itunes', ITUNES_NS)
        channel = ET.SubElement(rss, 'channel')
        ET.SubElement(channel, 'title').text = 'SuperCow AI News Podcast'
        ET.SubElement(channel, 'link').text = 'https://mommocmoc.github.io/supercow-ai-news-podcast'
        ET.SubElement(channel, 'description').text = 'Daily AI news generated by NotebookLM'
        ET.SubElement(channel, 'language').text = 'ko-KR'
        
        # Add podcast cover image
        itunes_image = ET.SubElement(channel, '{%s}image' % ITUNES_NS)
        itunes_image.set('href', image_url)
    else:
        tree = ET.parse(feed_file)
        rss = tree.getroot()
        channel = rss.find('channel')
        
        # Ensure itunes:image exists at channel level
        it_image = channel.find('{%s}image' % ITUNES_NS)
        if it_image is None:
            it_image = ET.SubElement(channel, '{%s}image' % ITUNES_NS)
        it_image.set('href', image_url)

    # Add new episode
    item = ET.SubElement(channel, 'item')
    ET.SubElement(item, 'title').text = title
    
    file_path = os.path.join('audio', mp3_filename)
    file_size = os.path.getsize(file_path) if os.path.exists(file_path) else 0
    
    ET.SubElement(item, 'enclosure', url=audio_url, type='audio/mpeg', length=str(file_size))
    ET.SubElement(item, 'pubDate').text = datetime.now().strftime("%a, %d %b %Y %H:%M:%S GMT")
    ET.SubElement(item, 'description').text = description
    ET.SubElement(item, '{%s}summary' % ITUNES_NS).text = description

    # Save fixed XML
    xml_str = ET.tostring(rss, encoding='utf-8').decode('utf-8')
    if xml_str.count('xmlns:itunes') > 1:
        rss.attrib.pop('xmlns:itunes', None)
        xml_str = ET.tostring(rss, encoding='utf-8').decode('utf-8')

    with open(feed_file, 'w', encoding='utf-8') as f:
        f.write('<?xml version="1.0" encoding="UTF-8"?>\n')
        f.write(xml_str)
        
    print(f"Updated {feed_file} with cover and episode {mp3_filename}")

if __name__ == "__main__":
    if len(sys.argv) < 4:
        print("Usage: python update_feed.py <title> <mp3_filename> <description>")
    else:
        update_podcast_feed(sys.argv[1], sys.argv[2], sys.argv[3])
